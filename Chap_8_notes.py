# Arrays allow you to store a group of items of the same data type together in memory.
# PYTHON DOES'NT HAVE a specific ARRAY. We gotta make a list to make "our version" of an array.

GENRE = ["rock_1", "rock_2", "rock_3", "rock_4", "rock_5"]
for idx in GENRE:           # saying that "call the list that we made"
    print(idx)              # print the list 
print("****************")   # A divider for when we target what we look for
print(GENRE[4])             # The target we're looking for






